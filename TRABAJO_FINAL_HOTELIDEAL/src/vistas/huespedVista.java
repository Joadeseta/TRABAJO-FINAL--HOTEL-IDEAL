    private void jBnuevoHuespedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBnuevoHuespedActionPerformed
        
        try {
            String dniS = jTextNumeroDNI.getText();
            String nombre = jTextNOMBRE.getText();
            String domicilio = jTextDOMICILIOHUESPED.getText();
            String correo = jTextCORREOHUESPED.getText();
            Integer celular = Integer.parseInt(jTextCELULARHUESPED.getText());
            boolean estado = jRadioESTADOHUESPED.isSelected();
            int dni = Integer.parseInt(dniS);
            
            if (dni <= 0 || nombre.isEmpty() || domicilio.isEmpty() || correo.isEmpty() || celular <= 0) {
                JOptionPane.showMessageDialog(this, "Ingrese datos vÃ¡lidos en todos los campos obligatorios.");
                return;
            }
            
            if (huespedActual == null) {
                
                Huesped huespedExistente = hueData.obtenerHuespedPorDNIYID(dni); // Reemplaza "id" con el ID del huÃ©sped actual

                if (huespedExistente == null) {
                    huespedActual = new Huesped(nombre, dni, domicilio, correo, celular, estado);
                    hueData.agregarHuesped(huespedActual); // Agregar un nuevo huÃ©sped a la base de datos
                    JOptionPane.showMessageDialog(this, "Huesped guardado exitosamente.");
                } else {
                    JOptionPane.showMessageDialog(this, "El huÃ©sped ya existe en la base de datos.");
                }
                
            }
            
            limpiarCampos();
            huespedActual = null;
            
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Ingrese un nÃºmero vÃ¡lido en el campo DNI.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar o modificar al huÃ©sped: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_jBnuevoHuespedActionPerformed

    private void jBELIMINARHUESPEDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBELIMINARHUESPEDActionPerformed
        if (huespedActual != null) {
            int confirmacion = JOptionPane.showConfirmDialog(this, "Â¿Seguro que desea eliminar este Huesped?", "Confirmar EliminaciÃ³n", JOptionPane.YES_NO_OPTION);
            
            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    // Intenta eliminar al alumno
                    hueData.eliminaHuesped(huespedActual.getIdHuesped());

                    // Limpia los campos y establece alumnoActual a null
                    limpiarCampos();
                    huespedActual = null;
                    
                    JOptionPane.showMessageDialog(this, "Huesped eliminado exitosamente.");
                } catch (Exception e) {
                    // Maneja las excepciones que puedan ocurrir durante la eliminaciÃ³n
                    JOptionPane.showMessageDialog(this, "Error al eliminar al huesped: ");
                }
            }
        } else {
            // Muestra un mensaje de advertencia si no se ha seleccionado un huesped
            JOptionPane.showMessageDialog(this, "No ha seleccionado ningÃºn huesped para eliminar.");
        }

    }//GEN-LAST:event_jBELIMINARHUESPEDActionPerformed

